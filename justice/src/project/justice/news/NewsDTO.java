package project.justice.news;

public class NewsDTO {

	private String news_title;
	private String news_url;
	private String news_date;
	private String news_press;
	private String news_reporter;
	private String news_field;
	private String news_keyword;
	private String news_connectionword;
	
	

	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getNews_url() {
		return news_url;
	}
	public void setNews_url(String news_url) {
		this.news_url = news_url;
	}
	public String getNews_date() {
		return news_date;
	}
	public void setNews_date(String news_date) {
		this.news_date = news_date;
	}
	public String getNews_field() {
		return news_field;
	}
	public void setNews_field(String news_field) {
		this.news_field = news_field;
	}
	public String getNews_keyword() {
		return news_keyword;
	}
	public void setNews_keyword(String news_keyword) {
		this.news_keyword = news_keyword;
	}
	public String getNews_connectionword() {
		return news_connectionword;
	}
	public void setNews_connectionword(String news_connectionword) {
		this.news_connectionword = news_connectionword;
	}
	public String getNews_press() {
		return news_press;
	}
	public void setNews_press(String news_press) {
		this.news_press = news_press;
	}
	public String getNews_reporter() {
		return news_reporter;
	}
	public void setNews_reporter(String news_reporter) {
		this.news_reporter = news_reporter;
	}

}
